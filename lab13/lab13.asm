; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.
;R0: Rprint		(Rprint)
;R1: Rff		(Rfont-file) 		(x5000)
;R2: Rcc		(Rcurrent-charcter)
;R3: Rasc		(Rascii-row)
;R4: Rsrc		(Rsource-row-content)
;R5: Rrow		(Rrow-counter)
;R6: Rcol		(Rcolumn-counter
;R7: avoid

		.ORIG x3000

		AND R0, R0, #0 ; reset R0
		AND R1, R1, #0 ; reset R1
		AND R2, R2, #0 ; reset R2
		AND R3, R3, #0 ; reset R3
		AND R4, R4, #0 ; reset R4
		AND R5, R5, #0 ; reset R5
		AND R6, R6, #0 ; reset R6

		ADD R6, R6, #8 ; set R6 (column counter) to 8 

		LD R1, START_ADDRESS ;starting address of the font file x5000
		
		ADD R2, R2, #2 ; set R2 (current-charcter) to 2
		
NEW_FULL_LINE	ADD R1, R1, R2 ; add Rff and Rcc to get the charcter
		
		LDR R3, R1, #0 ; load the charcter shape to R3 (R-asc)
	
		

TIMES_16	ADD R3, R3, R3 ; *2
		ADD R3, R3, R3 ; *4
		ADD R3, R3, R3 ; *8
		ADD R3, R3, R3 ; *16 (Rascx16)

		BRz CHECK_NEW ; if it is null just exit

		AND R1, R1, #0 ; reset Rff to 0

		LEA R1, FONT_DATA ; load the starting address of font data

		ADD R3, R3, R1 ; add starting address and Rasc*16

		AND R1, R1, #0 ; reset Rff to 0

		LD R1, START_ADDRESS ; starting address of the font file x5000
	
		ADD R3, R3, R5 ; Rasc + Rrow, add to the row currently at

		LDR R4, R3, #0 ; get the content from the row

IS_NEGATIVE	BRn PRINT_1 ; check if the content have 1 (negative)

PRINT_0 	LDR R0, R1, #0 ; print the thing representing 0
		OUT

DECRE_COL 	ADD R6, R6, #-1 ; column -1

CHECK_NEW	BRz PRINT_NEW_LINE ;

		ADD R4, R4, R4 ; left shift the content

		BRnzp IS_NEGATIVE ; unconditionally go back


EXIT 		HALT

PRINT_1 	LDR R0, R1, #1 ; print the thing representing 1
		OUT
		BRnzp DECRE_COL 

PRINT_NEW_LINE  ADD R2, R2, #1 ; increment to the next charcter

		ADD R1, R1, R2 ; add Rff and Rcc to get the charcter

		ADD R6, R6, #8 ; reset column counter back to 8
		
		LDR R3, R1, #0 ; load the charcter shape to R3 (R-asc)
		
		BRnp TIMES_16 ; if not null go back to do the next

		AND R1, R1, #0 ; reset Rff to 0

		LD R1, START_ADDRESS ; starting address of the font file x5000

		LD R0, ASCII_NL ; print new line
		OUT
		
		AND R2, R2, #0 ; reset R2

		ADD R2, R2, #2 ; set R2 (current-charcter) back to 2

		ADD R5, R5, #1 ; row count + 1

		ADD R0, R5, #-16 ; check if row count = 15

		BRnp NEW_FULL_LINE ; go back to do the new row

		BRnzp EXIT ; end the program if it is zero

		
	
