; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.
;R0: 
;R1: Rff (x5000)
;R2: R-asc -> Rx16
;R3: R-fd -> R-sr
;R4: R-sr-c
;R5: row counter
;R6: column counter
;R7: avoid

		.ORIG 3000

		AND R0, R0, #0; reset R0
		AND R1, R1, #0; reset R1
		AND R2, R2, #0; reset R2
		AND R3, R3, #0; reset R3
		AND R4, R4, #0; reset R4
		AND R5, R5, #0; reset R5
		AND R6, R6, #0; reset R6


		LD R1, START_ADDRESS
		; starting address of the font file x5000
		LDR R2, R1, #2; load the charcter shape to R2 x5002 (R-asc)
		ADD R2, R2, R2; *2
		ADD R2, R2, R2; *4
		ADD R2, R2, R2; *8
		ADD R2, R2, R2; *16 (R-x16)
		LEA R3, FONT_DATA 
		; get font data
		ADD R3, R3, R2; (R-sr)
		ADD R5, R5, #8; set R5 to 16 row counter
		ADD R5, R5, #8; set R5 to 16 row counter
SET_COLUMN	ADD R6, R6, #8; set R6 to 8 column counter
		LDR R4, R3, #0; get the data from font data
IS_NEGATIVE	BRn PRINT_1
PRINT_0 	LDR R0, R1, #0;
		OUT
DECRE_COL 	ADD R6, R6, #-1;
		BRz PRINT_NEW_LINE
		ADD R4, R4, R4;
		BRnzp IS_NEGATIVE


EXIT 		HALT

PRINT_1 	LDR R0, R1, #1;
		OUT
		BRnzp DECRE_COL 

PRINT_NEW_LINE  LD R0, ASCII_NL
		OUT
		ADD R3, R3, #1;
		ADD R5, R5, #-1;
		BRz EXIT
		BRnp SET_COLUMN
		
	
